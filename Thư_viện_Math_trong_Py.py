import math

x = float(input("Nhập vào giá trị của x = "))
print("|x| = ", math.fabs(x))  # Lấy trị tuyệt đối
print("sqrt(x) = ", math.sqrt(x))  # Lấy căn bậc 2
print("ceil(x) = ", math.ceil(x))  # Làm tròn lên giá trị phía trên vd: 4.3=5
print("floor(x) = ", math.floor(x))  # Làm tròn xuống giá trị phía dưới vd: 4.8=4
