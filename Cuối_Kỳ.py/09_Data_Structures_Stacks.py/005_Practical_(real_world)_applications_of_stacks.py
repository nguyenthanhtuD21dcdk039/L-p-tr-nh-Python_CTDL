def do_operation():  # Định nghĩa hàm do_operation()
    a = 5  # Khai báo biến a và gán giá trị là 5
    b = 10  #  Khai báo biến b và gán giá trị là 10
    print(a + b)  # In ra màn hình kết quả của phép cộng a + b


if __name__ == "__main__":  # Phần main chính thức chạy chương trình
    do_operation()  # Gọi đến hàm do_operation() để thực thi
